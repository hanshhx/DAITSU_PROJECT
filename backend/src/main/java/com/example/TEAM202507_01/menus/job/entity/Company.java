package com.example.TEAM202507_01.menus.job.entity;
// [1] 패키지 선언: 이 파일이 '일자리(job) 메뉴 > 엔티티(entity)' 폴더에 살고 있다는 주소입니다.
// Entity 패키지는 보통 DB 테이블과 직접 연결되는 중요한 파일들을 모아두는 곳입니다.

// [2] 임포트: 롬복(Lombok) 라이브러리들을 가져옵니다.
// 코드를 칠 때 반복되는 노가다 작업(Getter/Setter 만들기 등)을 대신 해주는 아주 고마운 도구들입니다.
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

// [3] 어노테이션(@Data): 종합 선물 세트입니다.
// 의미: "아래 변수들의 Getter(꺼내기), Setter(넣기), toString(출력하기), equals(비교하기) 코드를 자동으로 다 만들어줘."
// 효과: 코드가 매우 짧아지고, 나중에 필드가 추가되어도 알아서 업데이트됩니다.
@Data

// [4] 어노테이션(@Builder): 레고 조립 도우미입니다.
// 의미: "객체를 만들 때 Company.builder().name('네이버').build() 처럼 이름표를 붙여서 직관적으로 만들게 해줘."
// 효과: 생성자 파라미터 순서를 외울 필요가 없어서 실수를 줄여줍니다.
@Builder

// [5] 어노테이션(@NoArgsConstructor): 빈 상자 생성기입니다.
// 의미: "파라미터가 하나도 없는 기본 생성자(new Company())를 만들어줘."
// 중요: MyBatis나 JPA 같은 DB 도구들이 데이터를 읽어와서 객체로 만들 때, 일단 빈 객체부터 만들고 데이터를 채우기 때문에 이게 없으면 에러가 납니다.
@NoArgsConstructor

// [6] 어노테이션(@AllArgsConstructor): 꽉 찬 상자 생성기입니다.
// 의미: "모든 필드 값을 파라미터로 받는 생성자를 만들어줘."
// 중요: 위의 @Builder가 작동하려면 모든 필드를 다루는 생성자가 반드시 있어야 하기 때문에 짝꿍으로 따라다닙니다.
@AllArgsConstructor

// [7] 클래스 선언: "지금부터 DB의 'COMPANY' 테이블과 짝을 이룰 Company 클래스를 정의할게."
public class Company {

    // [8] 필드(id): 기본 키(Primary Key)입니다.
    // 의미: 이 회사의 주민등록번호 같은 고유 번호입니다.
    // 특징: 'Long' 타입을 쓴 이유는 회사가 수십억 개가 될 수도 있으니 넉넉하게 잡은 것이고,
    // 보통 DB에서 'Auto Increment'로 설정되어 있어서 우리가 입력 안 해도 1, 2, 3... 자동으로 생깁니다.
    private Long id;        // PK (DB에서 자동 생성)

    // [9] 필드(type): 외래 키(Foreign Key)입니다.
    // 의미: 기업 형태를 나타내는 숫자 코드입니다. (예: 1=대기업, 2=중견기업, 3=스타트업)
    // 특징: 여기에 "대기업"이라고 글자를 넣지 않고 숫자(ID)를 넣는 이유는,
    // 나중에 '기업 형태' 이름이 바뀌더라도(예: 중소기업 -> 강소기업) 숫자만 연결해두면 데이터 수정이 편하기 때문입니다.
    // (이 숫자는 JOB_TYPE 이라는 다른 테이블의 ID를 가리킵니다.)
    private Long type;      // 기업 형태 (FK -> JOB_TYPE의 ID, 예: 1)

    // [10] 필드(name): 회사 이름입니다.
    // 의미: 사용자에게 보여줄 실제 회사 명칭 (예: "카카오")
    private String name;    // 회사명

    // [11] 필드(address): 회사 주소입니다.
    // 의미: 회사가 어디 있는지 나타내는 문자열 (예: "제주특별자치도 제주시...")
    private String address; // 주소

    // [12] 필드(phone): 회사 대표 전화번호입니다.
    // 의미: 연락처 정보 (예: "02-1234-5678")
    // 특징: 전화번호는 숫자지만 계산할 일이 없고, 하이픈(-)이나 0으로 시작하는 경우가 있어서 문자열(String)로 저장합니다.
    private String phone;   // 전화번호
}

//
//        상황: 크롤러가 새로운 공고를 발견했을 때
//
//        발견: 크롤러가 "네이버"의 채용 공고를 긁어왔습니다.
//
//        중복 확인: DB에 "네이버"라는 회사가 이미 등록되어 있는지 확인합니다.
//
//        신규 등록 (Entity 사용):
//
//        만약 등록된 회사가 없다면, 이 Company 객체를 새로 만듭니다.
//
//        name="네이버", address="경기도 성남시..." 정보를 채워 넣습니다.
//
//        저장:
//
//        이 Company 객체를 매퍼에게 넘기면, 매퍼는 DB 테이블에 그대로 저장합니다.
//
//        이때 DB는 자동으로 id(고유 번호, 예: 501번)를 발급해줍니다.