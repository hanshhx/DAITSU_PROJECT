package com.example.TEAM202507_01.menus.hospital.dto;
// [1] 패키지 선언: 이 파일이 컴퓨터 안의 '병원 메뉴 > 데이터 가방(dto)' 폴더에 들어있다는 주소입니다.

import lombok.Data;
// [2] 임포트: 롬복(Lombok) 라이브러리의 @Data 기능을 쓰기 위해 도구를 가져옵니다.

// [3] 어노테이션(@Data): 이 클래스에 마법을 겁니다.
// - 의미: "개발자가 일일이 코드 칠 필요 없이, 아래 변수들에 값을 넣고(Setter), 꺼내고(Getter), 출력하는(ToString) 기능을 자동으로 만들어줘."
// - 효과: 코드가 매우 간결해집니다.
@Data
public class HospitalMapDto {
    // [4] 클래스 선언: "지금부터 지도 전용 병원 데이터 가방(HospitalMapDto)을 정의할게."

    private Long id;
    // 의미: 병원 고유 관리 번호 (PK)
    // 용도: 지도에서 핀을 눌렀을 때, "상세 페이지로 이동"하려면 이 병원이 몇 번인지 알아야 하니까 꼭 필요합니다.

    private String name;
    // 의미: 병원 이름
    // 용도: 지도 위에 핀이 꽂혀있을 때, 그 핀 위에 "대전 한국병원"처럼 이름을 띄워주기 위해 필요합니다.

    private String treatCategory;
    // 의미: 진료 과목 (내과, 치과 등)
    // 용도: 지도에서 "치과만 보기", "내과만 보기" 처럼 필터링 기능을 만들 때 사용하거나, 이름 옆에 작게 표시할 때 씁니다.

    private String address;
    // 의미: 주소 (가장 중요!)
    // 용도: 지도는 '위도/경도' 좌표가 있어야 핀을 찍을 수 있습니다.
    // 프론트엔드(화면)에서 이 주소 글자("대전 동구...")를 지도 API에 던지면 좌표로 바꿔줍니다(Geocoding).

    private String tel;
    // 의미: 병원 전화번호
    // 용도: 모바일에서 지도를 보고 바로 [전화 걸기] 버튼을 누를 수 있게 하기 위해 담습니다.
}

//        지도 로딩 요청: 사용자가 웹사이트에서 "지도로 보기" 버튼을 누릅니다.
//
//        가벼운 데이터 조회:
//
//        서버는 DB에서 병원 데이터를 꺼냅니다.
//
//        이때 모든 정보를 다 가져오지 않고, 위치(주소), 이름, 전화번호 같이 지도 표시에 딱 필요한 정보만 골라서 이 **HospitalMapDto**에 담습니다.
//
//        빠른 전송:
//
//        데이터가 가볍기 때문에(평점 계산 등을 안 하므로) 서버에서 사용자에게 아주 빠르게 전달됩니다.
//
//        핀 꽂기 (Geocoding):
//
//        사용자의 브라우저(프론트엔드)는 이 DTO에 들어있는 address(주소)를 보고, 지도 API(카카오맵 등)를 통해 위도/경도 좌표를 찾아서 지도 위에 핀(Marker)을 콕콕 찍습니다.
//
//        핀 클릭:
//
//        사용자가 핀을 누르면 name과 tel이 말풍선으로 뜨고, 더 자세히 보기를 누르면 id를 이용해 상세 페이지로 이동합니다.