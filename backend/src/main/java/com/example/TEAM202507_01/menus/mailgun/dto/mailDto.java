package com.example.TEAM202507_01.menus.mailgun.dto;
// [1] 패키지 선언: 이 파일이 컴퓨터 내의 '메일건(mailgun) 메뉴 > 데이터 가방(dto)' 폴더에 위치한다는 '집 주소'입니다.
// 나중에 다른 파일에서 이 클래스를 불러올 때 이 주소를 보고 찾아옵니다.

import lombok.Getter; // [2] 롬복 도구 가져오기: 값을 꺼내는 기능(Get)을 만들기 위해 가져옵니다.
import lombok.Setter; // [3] 롬복 도구 가져오기: 값을 넣는 기능(Set)을 만들기 위해 가져옵니다.

@Getter
// [4] 어노테이션(@Getter): "롬복아, 아래 변수들에 대해 '꺼내기 버튼(Getter)'을 자동으로 만들어줘."
// 역할: 이 코드가 있으면, 다른 클래스에서 mailDto.getTo() 라고 쓰면 'to' 변수 안에 있는 값을 읽을 수 있게 됩니다.
// 이게 없으면 데이터를 담아놓고도 꺼낼 수가 없어서 '읽기 전용'이 불가능해집니다.

@Setter
// [5] 어노테이션(@Setter): "롬복아, 아래 변수들에 대해 '넣기 구멍(Setter)'을 자동으로 만들어줘."
// 역할: 이 코드가 있으면, 스프링이 mailDto.setTo("abc@naver.com") 처럼 데이터를 집어넣을 수 있게 됩니다.
// 이게 없으면 프론트엔드에서 보낸 데이터를 이 객체 안에 담을 방법이 없습니다.

public class mailDto {
    // [6] 클래스 선언: "지금부터 메일 정보를 담을 'mailDto'라는 이름의 데이터 가방을 정의할게."
    // public: 어디서든(다른 패키지에서도) 이 가방을 가져다가 쓸 수 있다는 뜻입니다.

    // [7] 필드(to): 수신자 이메일
    // private: "이 변수는 소중하니까 외부에서 함부로 손대지 마. 꼭 정해진 문(Getter/Setter)으로만 출입해." (캡슐화/보안)
    // String: 이메일 주소는 "abc@gmail.com" 같은 문자열이니까 String 타입을 씁니다.
    private String to;    // 수신자 이메일 주소를 담는 변수이다.

    // [8] 필드(subject): 메일 제목
    // private: 위와 마찬가지로 외부의 직접 접근을 막습니다.
    // String: 제목도 "안녕하세요" 같은 문자열입니다.
    private String subject; // 메일 제목을 담는 변수이다.

    // [9] 필드(message): 메일 본문 내용
    // private: 본문 내용도 안전하게 보호합니다.
    // String: 본문은 아주 긴 문자열이 들어갈 수 있습니다.
    private String message; // 메일 본문 내용을 담는 변수이다.
}

//입력 (Frontend):
//
//사용자가 화면 입력창에 씁니다.
//
//받는 사람: friend@gmail.com
//
//제목: 안녕!
//
//내용: 잘 지내니?
//
//포장 (Controller):
//
//        [전송] 버튼을 누르면 이 데이터가 서버로 날아옵니다 (JSON 형식).
//
//스프링(서버)은 이 데이터를 받자마자 mailDto라는 빈 상자를 하나 꺼냅니다.
//
//그리고 JSON에 적힌 내용을 보고 상자에 채워 넣습니다. (Setter 사용)
//
//        "받는 사람 칸에는 friend@gmail.com 넣고..."
//
//배송 (Service):
//
//꽉 채워진 mailDto 상자는 mailService에게 전달됩니다.
//
//서비스는 상자를 열어서 내용물을 확인합니다. (Getter 사용)
//
//        "어디 보자, 받는 사람이 누구지? 아, friend@gmail.com이구나."
//
//발송 (Mailgun):
//
//확인한 정보를 바탕으로 실제 이메일 발송 서버(Mailgun)에 전송 요청을 보냅니다.